/**
 * Компонент, который реализует таблицу
 * с возможностью удаления строк
 *
 * Пример одного элемента, описывающего строку таблицы
 *
 *      {
 *          name: 'Ilia',
 *          age: 25,
 *          salary: '1000',
 *          city: 'Petrozavodsk'
 *      },
 *
 * @constructor
 */
export default class UserTable {

  constructor(rows) {
    this.elem = document.createElement('table');

    let headTemplate = `
      <thead>
        <tr>
          <th>Имя</th>
          <th>Возраст</th>
          <th>Зарплата</th>
          <th>Город</th>
          <th></th>
        </tr>
      </thead>
    `;

    let bodyTemplate = '';

    rows.forEach((item) => {
      let template = `
        <tr>
          <td>${item.name}</td>
          <td>${item.age}</td>
          <td>${item.salary}</td>
          <td>${item.city}</td>
          <td><button>X</button></td>
        </tr>
      `;

      bodyTemplate += template;
    });

    this.elem.innerHTML = headTemplate + bodyTemplate;

    this.elem.addEventListener('click', (e) => {
      if (e.target.tagName === 'BUTTON') {
        let row = e.target.closest('tr');
        row.remove();
      }
    });
  }
}
